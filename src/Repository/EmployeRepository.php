     * Rechercher des employés par nom ou prénom
     */
    public function findBySearchTerm(string $term)
    {
        return $this->createQueryBuilder('e')
            ->where('e.nom LIKE :term OR e.prenom LIKE :term')
            ->setParameter('term', '%' . $term . '%')
            ->getQuery()
            ->getResult();
